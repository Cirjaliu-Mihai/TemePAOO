package org.example;

public class C3Nr {
    private int nr1;
    private int nr2;
    private int nr3;

    public C3Nr(int nr1, int nr2, int nr3) {
        this.nr1 = nr1;
        this.nr2 = nr2;
        this.nr3 = nr3;
    }

    public int getSum() {
        return nr1 + nr2 + nr3;
    }
}

